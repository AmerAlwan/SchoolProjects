
public class MathOperators {
	public static void main(String[] args) {
		System.out.println(7 / 3); //2
		System.out.println((float) 7 / 3); //2.3
		System.out.println(7 / (float) 3); //2.3
		System.out.println((double) 7 / 3); //2.333333
		System.out.println(7 / (double) 3); //2.333333
		System.out.println(7 / 3.0); //2.3
		System.out.println(7.0 / 3); //2.3
		
		float ans;
		ans = 7 / 3;
		System.out.println(ans); //2.0
		
		System.out.println("355 / 113 = " + ((double) 355 / 113));
	
	}

}
